package org.dflib.echarts.render.option;

public class EncodeModel {

    private final int x;
    private final int y;

    public EncodeModel(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
